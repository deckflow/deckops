export interface DeckTask<T extends DeckTaskType = DeckTaskType> {
  /** Task id generated by the backend. */
  id: string;
  /** Space/workspace id owning this task. */
  spaceId: string;
  /** Task type. */
  type: T;
  /** Current task status. */
  status: DeckTaskStatus;
  /** Ordered input file ids. */
  fileIds?: string[];
  /** Task display name. */
  name?: string;
  /** Task parameters. */
  params?: DeckTaskTypeParams[T];
  /** Optional preview payload. */
  preview?: DeckTaskTypePreview[T];
  /** Task result payload when the backend includes it in task detail responses. */
  result?: DeckTaskTypeResult[T] | null;
  /** Error message for failed tasks. */
  error?: string | null;
  /** ISO create timestamp. */
  createdAt?: string;
  /** ISO update timestamp. */
  updatedAt?: string;
}

